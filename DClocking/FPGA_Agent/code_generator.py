"""Code generator — produces VHDL + Python code for new FPGA modules.

Generates:
- VHDL core entity (skeleton — signal processing logic must be filled by user)
- VHDL module wrapper (bus_handler + parameter_ram + core instantiation)
- Python hardware class (ModuleBase subclass)
- Python Qt NodeItem class (NodeItem subclass)
- Parameter schema
- Runtime registration injections
"""

